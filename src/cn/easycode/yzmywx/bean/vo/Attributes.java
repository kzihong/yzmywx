package cn.easycode.yzmywx.bean.vo;

public class Attributes implements java.io.Serializable  {
	private static final long serialVersionUID = -7751590216890727868L;
	private String url;
	private String target;

	public Attributes() { }

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}
	

}
